class Translator:
    def __init__(self, language='hr'):
        self.language = language if language in LANGUAGES else 'hr'

    def set_language(self, language):
        self.language = language if language in LANGUAGES else 'hr'

    def __call__(self, text, **values):
        translated = ENGLISH.get(text, text) if self.language == 'en' else text
        return translated.format(**values) if values else translated
